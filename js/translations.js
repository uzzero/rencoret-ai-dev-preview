// Language management system
function LanguageManager() {
    var self = this;
    this.currentLanguage = 'de';
    this.supportedLanguages = ['de', 'en', 'es'];

    this.init = function () {
        // Load saved language or detect from browser
        self.currentLanguage = self.getSavedLanguage() || self.detectBrowserLanguage();
        if (!self.supportedLanguages.includes(self.currentLanguage)) {
            self.currentLanguage = 'de';
        }

        // Set initial language
        document.documentElement.lang = self.currentLanguage;

        // Initialize when DOM is ready
        if (document.readyState === 'loading') {
            document.addEventListener('DOMContentLoaded', function () {
                self.bindLanguageSelectors();
                self.updatePageContent();
                self.updateLanguageSelector();
            });
        } else {
            // DOM is already loaded
            self.bindLanguageSelectors();
            self.updatePageContent();
            self.updateLanguageSelector();
        }
    }

    this.detectBrowserLanguage = function () {
        var browserLang = navigator.language || navigator.languages[0];
        var langCode = browserLang.toLowerCase().substring(0, 2);

        // Map common language codes to our supported languages
        var languageMap = {
            'de': 'de',
            'en': 'en',
            'es': 'es',
            'us': 'en',
            'gb': 'en',
            'uk': 'en',
            'mx': 'es',
            'ar': 'es',
            'co': 'es',
            'cl': 'es',
            'pe': 'es'
        };

        return languageMap[langCode] || 'de'; // Default to German
    }

    this.getSavedLanguage = function () {
        return localStorage.getItem('selectedLanguage');
    }

    this.saveLanguage = function (lang) {
        localStorage.setItem('selectedLanguage', lang);
    }

    this.setLanguage = function (lang) {
        if (!self.supportedLanguages.includes(lang)) {
            lang = 'de';
        }

        self.currentLanguage = lang;
        self.saveLanguage(lang);

        // Update HTML lang attribute
        document.documentElement.lang = lang;

        // Update all translatable elements
        self.updatePageContent();

        // Update language selector
        self.updateLanguageSelector();

        // Update meta tags
        self.updateMetaTags();

        // Dispatch custom event
        document.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: lang } }));
    }

    this.updatePageContent = function () {
        // Add a small delay to ensure all elements are loaded
        setTimeout(() => {
            const elements = document.querySelectorAll('[data-i18n]');

            for (var i = 0; i < elements.length; i++) {
                var element = elements[i];
                var key = element.getAttribute('data-i18n');
                var translation = self.getTranslation(key);

                if (translation) {
                    // Handle different element types
                    if (element.tagName === 'INPUT' && element.type === 'submit') {
                        element.value = translation;
                    } else if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                        element.placeholder = translation;
                    } else if (element.tagName === 'META') {
                        element.content = translation;
                    } else {
                        // Handle text with HTML preservation
                        if (element.hasAttribute('data-i18n-html')) {
                            element.innerHTML = translation;
                        } else {
                            element.textContent = translation;
                        }
                    }
                }
            }

            // Update elements with special handling
            self.updateSpecialElements();
        }, 100);
    }

    this.updateSpecialElements = function () {
        // Update hero heading with gradient
        var heroHeading = document.querySelector('.hero-heading');
        if (heroHeading) {
            var prefix = self.getTranslation('hero.heading');
            var gradient = self.getTranslation('hero.heading.gradient');
            var suffix = self.getTranslation('hero.heading.suffix');
            heroHeading.innerHTML = prefix + ' <span class="gradient-text">' + gradient + '</span> ' + suffix;
        }

        // Update privacy policy checkbox label
        var privacyLabel = document.querySelector('label[for="datenschutz"]');
        if (privacyLabel) {
            var prefix = self.getTranslation('contact.form.privacy');
            var linkText = self.getTranslation('contact.form.privacy.link');
            var suffix = self.getTranslation('contact.form.privacy.suffix');
            var link = self.currentLanguage === 'de' ? 'datenschutz.html' : 'datenschutz.html?lang=' + self.currentLanguage;
            privacyLabel.innerHTML = prefix + ' <a href="' + link + '">' + linkText + '</a> ' + suffix;
        }
    }

    this.updateMetaTags = function () {
        // Update title
        document.title = self.getTranslation('meta.title');

        // Update meta description
        var metaDescription = document.querySelector('meta[name="description"]');
        if (metaDescription) {
            metaDescription.content = self.getTranslation('meta.description');
        }

        // Update Open Graph tags
        var ogTitle = document.querySelector('meta[property="og:title"]');
        if (ogTitle) {
            ogTitle.content = self.getTranslation('meta.title');
        }

        var ogDescription = document.querySelector('meta[property="og:description"]');
        if (ogDescription) {
            ogDescription.content = self.getTranslation('meta.description');
        }

        // Add hreflang tags
        self.updateHreflangTags();
    }

    this.updateHreflangTags = function () {
        // Remove existing hreflang tags
        var existingHreflang = document.querySelectorAll('link[rel="alternate"][hreflang]');
        for (var i = 0; i < existingHreflang.length; i++) {
            existingHreflang[i].remove();
        }

        // Add new hreflang tags
        const head = document.querySelector('head');
        for (var i = 0; i < self.supportedLanguages.length; i++) {
            var lang = self.supportedLanguages[i];
            var link = document.createElement('link');
            link.rel = 'alternate';
            link.hreflang = lang;
            link.href = 'https://uzzero.github.io/rencoret-ai-dev-preview' + (lang !== 'de' ? '?lang=' + lang : '');
            head.appendChild(link);
        }

        var defaultLink = document.createElement('link');
        defaultLink.rel = 'alternate';
        defaultLink.hreflang = 'x-default';
        defaultLink.href = 'https://uzzero.github.io/rencoret-ai-dev-preview/';
        head.appendChild(defaultLink);
    }

    this.updateLanguageSelector = function () {
        // Update desktop language selector
        var selector = document.querySelector('.language-selector');
        if (selector) {
            var currentLangElement = selector.querySelector('.current-language');
            if (currentLangElement) {
                var langDisplay = {
                    'de': '🇩🇪 DE',
                    'en': '🇬🇧 EN',
                    'es': '🇪🇸 ES'
                };
                currentLangElement.textContent = langDisplay[self.currentLanguage];
            }

            // Update active state in dropdown
            var options = selector.querySelectorAll('.language-option');
            for (var i = 0; i < options.length; i++) {
                var option = options[i];
                if (option.getAttribute('data-lang') === self.currentLanguage) {
                    option.classList.add('active');
                } else {
                    option.classList.remove('active');
                }
            }
        }

        // Update mobile language selector
        var mobileOptions = document.querySelectorAll('.mobile-language-option');
        for (var j = 0; j < mobileOptions.length; j++) {
            var mobileOption = mobileOptions[j];
            if (mobileOption.getAttribute('data-lang') === self.currentLanguage) {
                mobileOption.classList.add('active');
            } else {
                mobileOption.classList.remove('active');
            }
        }
    }

    this.bindLanguageSelectors = function () {
        var languageOptions = document.querySelectorAll('[data-lang]');
        for (var i = 0; i < languageOptions.length; i++) {
            var option = languageOptions[i];
            option.addEventListener('click', function (e) {
                e.preventDefault();
                var lang = this.getAttribute('data-lang');
                self.setLanguage(lang);

                // Close the dropdown after selection
                var languageSelector = document.querySelector('.language-selector');
                if (languageSelector) {
                    languageSelector.classList.remove('active');
                    var currentLanguage = languageSelector.querySelector('.current-language');
                    if (currentLanguage) {
                        currentLanguage.setAttribute('aria-expanded', 'false');
                    }
                }
            });
        }
    }

    this.getTranslation = function (key) {
        var languageTranslations = translations[self.currentLanguage] || translations.de;
        return languageTranslations[key] || translations.de[key] || null;
    }

    // Initialize automatically
    this.init();
}

// Initialize language manager
var languageManager = new LanguageManager();

// Export for use in other scripts
window.languageManager = languageManager;
